public class variaveis {

    public static void main(String[] args) {

        // Declaração de variáveis

        int numero = 17; // Variável do tipo inteiro
        System.out.println("A variável número tem valor: " + numero);

        // Float --> Para uso com números decimais (ponto flutuante)

        float altura = (float) 1.72;
        System.out.println("Eu tenho " + altura + "m.");

        // Double --> Idem float só que tem uma maior precisão

        double preco = 99.99;
        System.out.println("O produto custa R$: " + preco);

        // String --> Armazena texto

        String nome = "Gustavo Molina";
        System.out.println("O conteúdo da variável é: " + nome);

        // Char --> Armazenar apenas 1 caracter

        char letra = 'G';
        System.out.println("O caracter armazenado é: " + letra);

    }

}