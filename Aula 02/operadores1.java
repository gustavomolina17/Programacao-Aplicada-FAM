/*

Operadores de Atribuição: O operador de atribuição é utilizado para definir o valor inicial ou sobrescrever o valor de uma variável. Em seu uso, o operando à esquerda representa a variável para a qual desejamos atribuir o valor informado à direita.

*/

public class operadores1 {

    public static void main(String[] args) {

        int lado = 2;
        float pi = 3.1426F;
        String texto = "Programação Aplicada";
        lado = 3;

        System.out.println("lado: " + lado);
        System.out.println("PI: " + pi);
        System.out.println("texto: " + texto);

    }
}