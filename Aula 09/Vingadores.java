// Criação da classe base (que será utilizada na herança)

public class Vingadores {
    public String nome;
    public int idade;
    public boolean armadura;
    public double altura;

    // Método Construtor

    public Vingadores(String nome, int idade, boolean armadura, double altura) {
        this.nome = nome;
        this.idade = idade;
        this.armadura = true;
        this.altura = altura;
    }

}
