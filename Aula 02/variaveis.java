public class variaveis {

    public static void main(String[] args) {

        // Declaração de Variáveis

        int numero = 10; // Variável do tipo inteiro.
        System.out.println("A variável 'número' tem valor: " + numero);

        // Float --> Para uso com números decimais (ponto flutuante).
        float altura = (float) 1.72;
        System.out.println("Eu tenho " + altura + " m.");

        // Double --> Para uso com números decimais (ponto flutuante).
        // Apresenta uma precisão > que float.
        double preco = 137.6;
        System.out.println("O produto custa R$ " + preco);

        // String --> Armazena texto.
        String nome = "Gustavo Molina";
        System.out.println("O conteúdo armazenado na variável 'nome' é: " + nome);

        // Char --> Armazena apenas 1 caracter.
        char letra = 'C';
        System.out.println("Char armazena apenas 1 caracter. O valor armazenado em 'letra' é: " + letra);

    }

}