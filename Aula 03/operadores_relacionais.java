public class operadores_relacionais {

    public static void main(String[] args) {

        int valorA = 1;
        int valorB = 2;

        if (valorA > valorB) {
            System.out.println("O valor 'A' é maior que 'B'.");
        }

        if (valorB > valorA) {
            System.out.println("O valor 'B' é maior que 'A'.");
        }

        if (valorA < valorB) {
            System.out.println("O valor 'A' é menor que 'B'.");
        }

        if (valorB < valorA) {
            System.out.println("O valor 'B' é menor que 'A'.");
        }

    }

}
